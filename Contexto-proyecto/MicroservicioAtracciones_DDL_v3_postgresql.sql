CREATE TABLE dbo.ticket (
    tck_id                      INTEGER         GENERATED BY DEFAULT AS IDENTITY,
    tck_guid                    UUID            NOT NULL DEFAULT gen_random_uuid(),
    at_id                       INTEGER         NOT NULL,
    tck_titulo                  VARCHAR(150)    NOT NULL,
    tck_precio                  NUMERIC(10,2)   NOT NULL,
    tck_tipo_participante       VARCHAR(30)     NOT NULL DEFAULT 'Adulto',
    tck_capacidad_maxima        INTEGER         NOT NULL,
    tck_cupos_disponibles       INTEGER         NOT NULL,

    -- Auditoría ingreso
    tck_fecha_ingreso           TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    tck_usuario_ingreso         VARCHAR(100)    NOT NULL,
    tck_ip_ingreso              VARCHAR(45)     NOT NULL,

    -- Auditoría modificación
    tck_fecha_mod               TIMESTAMPTZ     NULL,
    tck_usuario_mod             VARCHAR(100)    NULL,
    tck_ip_mod                  VARCHAR(45)     NULL,

    -- Auditoría eliminación lógica
    tck_fecha_eliminacion       TIMESTAMPTZ     NULL,
    tck_usuario_eliminacion     VARCHAR(100)    NULL,
    tck_ip_eliminacion          VARCHAR(45)     NULL,

    -- Estado
    tck_estado                  CHAR(1)         NOT NULL DEFAULT 'A',

    -- Constraints
    CONSTRAINT pk_ticket PRIMARY KEY (tck_id),
    CONSTRAINT uk_ticket_guid UNIQUE (tck_guid),
    CONSTRAINT ck_ticket_estado CHECK (tck_estado IN ('A','I')),
    CONSTRAINT ck_ticket_precio CHECK (tck_precio >= 0),
    CONSTRAINT ck_ticket_capacidad CHECK (tck_capacidad_maxima > 0),
    CONSTRAINT ck_ticket_cupos CHECK (
        tck_cupos_disponibles >= 0
        AND tck_cupos_disponibles <= tck_capacidad_maxima
    ),
    CONSTRAINT ck_ticket_tipo_participante CHECK (tck_tipo_participante IN ('Adulto','Niño','Grupo','Estudiante','Senior')),
    CONSTRAINT ck_ticket_eliminacion CHECK (
        (tck_estado = 'I' AND tck_fecha_eliminacion IS NOT NULL)
        OR (tck_estado = 'A' AND tck_fecha_eliminacion IS NULL)
    ),
    CONSTRAINT fk_ticket_atraccion FOREIGN KEY (at_id) REFERENCES dbo.atraccion (at_id)
);

