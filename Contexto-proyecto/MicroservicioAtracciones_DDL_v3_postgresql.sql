-- ------------------------------------------------------------
--  1.4  CLIENTES
-- ------------------------------------------------------------
CREATE TABLE dbo.clientes (
    cli_id                      INTEGER         GENERATED BY DEFAULT AS IDENTITY,
    cli_guid                    UUID            NOT NULL DEFAULT gen_random_uuid(),
    usu_id                      INTEGER         NOT NULL,

    -- Datos personales / empresariales
    cli_tipo_identificacion     VARCHAR(20)     NOT NULL,
    cli_numero_identificacion   VARCHAR(20)     NOT NULL,
    cli_nombres                 VARCHAR(100)    NULL,
    cli_apellidos               VARCHAR(100)    NULL,
    cli_razon_social            VARCHAR(200)    NULL,
    cli_correo                  VARCHAR(150)    NOT NULL,
    cli_telefono                VARCHAR(20)     NULL,
    cli_direccion               VARCHAR(300)    NULL,

    -- Auditoría ingreso
    cli_fecha_ingreso           TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cli_usuario_ingreso         VARCHAR(100)    NOT NULL,
    cli_ip_ingreso              VARCHAR(45)     NOT NULL,

    -- Auditoría eliminación lógica
    cli_fecha_eliminacion       TIMESTAMPTZ     NULL,
    cli_usuario_eliminacion     VARCHAR(100)    NULL,
    cli_ip_eliminacion          VARCHAR(45)     NULL,

    -- Estado y concurrencia
    cli_estado                  CHAR(1)         NOT NULL DEFAULT 'A',
    cli_row_version             BIGINT          NOT NULL DEFAULT 1,

    -- Constraints
    CONSTRAINT pk_clientes PRIMARY KEY (cli_id),
    CONSTRAINT uk_clientes_guid UNIQUE (cli_guid),
    CONSTRAINT uk_clientes_num_identificacion UNIQUE (cli_numero_identificacion),
    CONSTRAINT ck_clientes_estado CHECK (cli_estado IN ('A','I')),
    CONSTRAINT ck_clientes_tipo_id CHECK (cli_tipo_identificacion IN ('CC','RUC','PASAPORTE','CEDULA','OTRO')),
    CONSTRAINT ck_clientes_persona CHECK (
        (cli_razon_social IS NOT NULL)
        OR (cli_nombres IS NOT NULL AND cli_apellidos IS NOT NULL)
    ),
    CONSTRAINT ck_clientes_eliminacion CHECK (
        (cli_estado = 'I' AND cli_fecha_eliminacion IS NOT NULL)
        OR (cli_estado = 'A' AND cli_fecha_eliminacion IS NULL)
    ),
    CONSTRAINT fk_clientes_usuario FOREIGN KEY (usu_id) REFERENCES dbo.usuario (usu_id)
);

