-- ============================================================
--  TRIGGERS PARA ROWVERSION EMULADO
-- ============================================================

CREATE OR REPLACE FUNCTION dbo.fn_increment_row_version_generic()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_ARGV[0] = 'cli_row_version' THEN
        NEW.cli_row_version := OLD.cli_row_version + 1;
    ELSIF TG_ARGV[0] = 'fac_row_version' THEN
        NEW.fac_row_version := OLD.fac_row_version + 1;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_clientes_row_version
BEFORE UPDATE ON dbo.clientes
FOR EACH ROW
EXECUTE FUNCTION dbo.fn_increment_row_version_generic('cli_row_version');

CREATE TRIGGER trg_facturas_row_version
BEFORE UPDATE ON dbo.facturas
FOR EACH ROW
EXECUTE FUNCTION dbo.fn_increment_row_version_generic('fac_row_version');

