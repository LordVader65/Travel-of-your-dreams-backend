-- ============================================================
--  MICROSERVICIO DE ATRACCIONES
--  Script DDL v1.0 — Creación completa del esquema
--  Conversión de SQL Server a PostgreSQL
-- ============================================================
--  Convenciones:
--    · Todas las fechas se almacenan en UTC
--    · Estado: CHAR(1) con CHECK IN ('A','I')
--    · IPs: VARCHAR(45) cubre IPv4 e IPv6
--    · GUIDs expuestos en API; IDs internos nunca expuestos
--    · Soft-delete via _estado + _fecha_eliminacion
--    · ROWVERSION en tablas con concurrencia optimista
--    · Orden de creación respeta dependencias de FK
-- ============================================================

-- PostgreSQL:
--   · No existe USE ni GO.
--   · Se crea el schema dbo para respetar los nombres del script original.
--   · Para UUID se usa gen_random_uuid() (extensión pgcrypto).
--   · ROWVERSION de SQL Server se emula con BIGINT + trigger autoincremental por UPDATE.

CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE SCHEMA IF NOT EXISTS dbo;

-- ============================================================
--  FUNCIONES DE APOYO
-- ============================================================

-- ============================================================
--  SECCIÓN 1: IDENTIDAD Y ACCESO
--  Orden: ROLES → USUARIO → USUARIOXROLES → CLIENTES
-- ============================================================

-- ------------------------------------------------------------
--  1.1  ROLES
-- ------------------------------------------------------------
CREATE TABLE dbo.roles (
    rol_id                  INTEGER         GENERATED BY DEFAULT AS IDENTITY,
    rol_guid                UUID            NOT NULL DEFAULT gen_random_uuid(),
    rol_descripcion         VARCHAR(80)     NOT NULL,

    -- Auditoría ingreso
    rol_fecha_ingreso       TIMESTAMPTZ     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    rol_usuario_ingreso     VARCHAR(100)    NOT NULL,
    rol_ip_ingreso          VARCHAR(45)     NOT NULL,

    -- Auditoría eliminación lógica
    rol_fecha_eliminacion   TIMESTAMPTZ     NULL,
    rol_usuario_eliminacion VARCHAR(100)    NULL,
    rol_ip_eliminacion      VARCHAR(45)     NULL,

    -- Estado
    rol_estado              CHAR(1)         NOT NULL DEFAULT 'A',

    -- Constraints
    CONSTRAINT pk_roles PRIMARY KEY (rol_id),
    CONSTRAINT uk_roles_guid UNIQUE (rol_guid),
    CONSTRAINT ck_roles_estado CHECK (rol_estado IN ('A','I')),
    CONSTRAINT ck_roles_eliminacion CHECK (
        (rol_estado = 'I' AND rol_fecha_eliminacion IS NOT NULL)
        OR (rol_estado = 'A' AND rol_fecha_eliminacion IS NULL)
    )
);

