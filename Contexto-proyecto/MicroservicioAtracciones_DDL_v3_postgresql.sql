-- ------------------------------------------------------------
--  1.3  USUARIOXROLES
-- ------------------------------------------------------------
CREATE TABLE dbo.usuarioxroles (
    usu_rol_id              INTEGER         GENERATED BY DEFAULT AS IDENTITY,
    usu_id                  INTEGER         NOT NULL,
    rol_id                  INTEGER         NOT NULL,
    usu_rol_estado          CHAR(1)         NOT NULL DEFAULT 'A',

    -- Constraints
